package cn.com.checknull.model.vo; 
public class Select2 {
	private Object id;
	private String text;
	public Select2(Object id, String text) {
		super();
		this.id = id;
		this.text = text;
	}
	public Object getId() {
		return id;
	}
	public void setId(Object id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
 